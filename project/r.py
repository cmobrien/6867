def get_dist_probit(pred, mA, mB):
  A = 1 - norm.cdf(mA - pred)
  B = norm.cdf(mA - pred) - norm.cdf(mB - pred)
  C = norm.cdf(mB - pred)
  return (A, B, C)

def logit(x):
  return 1.0 / (1 + math.pow(math.e, -1 * x))

def get_dist_logit(pred, mA, mB):
  A = 1 - logit(mA - pred)
  B = logit(mA - pred) - logit(mB - pred)
  C = logit(mB - pred)
  return (A, B, C)


